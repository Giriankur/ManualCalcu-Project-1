canvas.create_line(0,0,1000,1000,fill="white",width=10)
canvas.create_rectangle(0,0,200,200,fill="blue")
canvas.create_oval(300,300,600,600,fill="yellow",outline="blue",width=5)
canvas.create_arc(300,500,600,200,extent=75,width=5)
#point =(250,110,280,280,250,110)
